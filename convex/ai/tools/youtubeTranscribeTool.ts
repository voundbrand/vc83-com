export const transcribeYoutubeVideoTool: AITool = {
  name: "transcribe_youtube_video",
  description:
    "Extract transcript text from a YouTube video URL. Supports captions-first mode and audio transcription fallback when captions are unavailable.",
  status: "ready",
  readOnly: true,
  parameters: {
    type: "object",
    properties: {
      videoUrl: {
        type: "string",
        description:
          "YouTube video URL (watch, youtu.be, shorts, live, or embed format).",
      },
      source: {
        type: "string",
        enum: ["auto", "captions", "audio"],
        description:
          "Transcript source mode: auto (captions first, then audio fallback), captions only, or audio only.",
      },
      language: {
        type: "string",
        description:
          "Optional preferred language code (e.g., 'en', 'es', 'de'). Used for caption selection and passed to audio transcription when provided.",
      },
      includeTimestamps: {
        type: "boolean",
        description:
          "When true, include timestamped transcript segments.",
      },
      maxCharacters: {
        type: "number",
        description:
          "Maximum transcript characters to return (500-50000). Defaults to 12000.",
      },
      maxSegments: {
        type: "number",
        description:
          "Maximum timestamped segments to return when includeTimestamps=true (10-500). Defaults to 120.",
      },
      saveToUserFiles: {
        type: "boolean",
        description:
          "When true, save transcript into the user's files as a markdown note.",
      },
      saveParentPath: {
        type: "string",
        description:
          "Optional destination folder path in user files (default '/').",
      },
      saveFileName: {
        type: "string",
        description:
          "Optional transcript filename (for example, 'meeting-transcript.md').",
      },
    },
    required: ["videoUrl"],
  },
  execute: async (
    ctx: ToolExecutionContext,
    args: {
      videoUrl: string;
      source?: TranscriptSource;
      language?: string;
      includeTimestamps?: boolean;
      maxCharacters?: number;
      maxSegments?: number;
      saveToUserFiles?: boolean;
      saveParentPath?: string;
      saveFileName?: string;
    }
  ) => {
    const videoUrl = normalizeOptionalString(args.videoUrl);
    if (!videoUrl) {
      return {
        success: false,
        error: "MISSING_VIDEO_URL",
        message: "Provide a YouTube URL in `videoUrl`.",
      };
    }

    const videoId = extractYouTubeVideoId(videoUrl);
    if (!videoId) {
      return {
        success: false,
        error: "INVALID_YOUTUBE_URL",
        message:
          "Could not extract a valid YouTube video ID from the provided URL.",
      };
    }

    const source = normalizeTranscriptSource(args.source);
    const maxCharacters = clampNumber(args.maxCharacters, {
      min: 500,
      max: 50_000,
      fallback: 12_000,
    });
    const maxSegments = clampNumber(args.maxSegments, {
      min: 10,
      max: 500,
      fallback: 120,
    });

    try {
      const metadata = await fetchVideoMetadata(videoId);
      const availableLanguages = Array.from(
        new Set(metadata.tracks.map((item) => item.languageCode))
      );
      const canonicalUrl = `https://www.youtube.com/watch?v=${videoId}`;
      let captionFailureMessage: string | undefined;

      if (source !== "audio") {
        if (metadata.tracks.length > 0) {
          const selection = selectCaptionTrack(metadata.tracks, args.language);
          if (selection) {
            const track = selection.track;
            const captionUrl = track.baseUrl
              ? normalizeTrackBaseUrl(track.baseUrl)
              : buildTimedTextJson3Url(videoId, track);

            const captionResponse = await fetch(captionUrl);
            if (captionResponse.ok) {
              const captionPayload = await captionResponse.json();
              const segments = extractCaptionSegmentsFromJson3(captionPayload);
              if (segments.length > 0) {
                const fullTranscript = normalizeCaptionText(
                  segments.map((segment) => segment.text).join(" ")
                );
                const truncated = truncateTranscript(fullTranscript, maxCharacters);
                const transcriptFile = args.saveToUserFiles
                  ? await saveTranscriptToUserFiles(ctx, {
                      transcript: fullTranscript,
                      source: "youtube_captions",
                      title: metadata.title,
                      language: track.languageCode,
                      parentPath: args.saveParentPath,
                      fileName: args.saveFileName,
                      metadata: {
                        videoId,
                        canonicalUrl,
                        sourceUsed: "captions",
                        isAutoGenerated: track.isAutoGenerated,
                      },
                    })
                  : undefined;

                return {
                  success: true,
                  sourceUsed: "captions",
                  videoId,
                  canonicalUrl,
                  title: metadata.title,
                  language: track.languageCode,
                  requestedLanguage: args.language || null,
                  isAutoGenerated: track.isAutoGenerated,
                  availableLanguages,
                  segmentCount: segments.length,
                  transcriptLength: fullTranscript.length,
                  maxCharacters,
                  isTruncated: truncated.isTruncated,
                  transcript: truncated.transcript,
                  fallbackNote: selection.fallbackReason,
                  transcriptFile,
                  segments: args.includeTimestamps
                    ? segments.slice(0, maxSegments).map((segment) => ({
                        startMs: segment.startMs,
                        durationMs: segment.durationMs,
                        startTime: formatTimestamp(segment.startMs),
                        text: segment.text,
                      }))
                    : undefined,
                };
              }

              captionFailureMessage =
                "Caption track exists, but no transcript text was returned.";
            } else {
              captionFailureMessage =
                `Failed to fetch captions (${captionResponse.status}).`;
            }
          } else {
            captionFailureMessage = "Could not select a caption track for this video.";
          }
        } else {
          captionFailureMessage =
            "No transcript/captions were found for this video.";
        }

        if (source === "captions") {
          return {
            success: false,
            error: "CAPTION_TRANSCRIPT_UNAVAILABLE",
            videoId,
            canonicalUrl,
            title: metadata.title,
            availableLanguages,
            audioFallbackAvailable:
              metadata.audioStreams.length > 0
              || metadata.encryptedSignatureStreams.length > 0,
            audioFallbackRequiresDecipher:
              metadata.audioStreams.length === 0
              && metadata.encryptedSignatureStreams.length > 0,
            message:
              captionFailureMessage
              || "Caption transcript unavailable for this video.",
          };
        }
      }

      let bestAudioStream = selectBestAudioStream(metadata.audioStreams);
      let decipherStrategyUsed: YouTubeSignatureDecipherStrategyId | null = null;

      if (!bestAudioStream && metadata.encryptedSignatureStreams.length > 0) {
        const decipherConfig = resolveSignatureCipherDecipherConfigFromEnv();
        const decipherStrategy = createSignatureCipherDecipherStrategy(decipherConfig);
        const decipherOutcome = await resolveEncryptedSignatureAudioStream({
          videoId,
          streams: metadata.encryptedSignatureStreams,
          strategy: decipherStrategy,
        });

        if (decipherOutcome.success) {
          bestAudioStream = decipherOutcome.stream;
          decipherStrategyUsed = decipherOutcome.decipherStrategy;
        } else {
          return {
            success: false,
            error: decipherOutcome.code,
            videoId,
            canonicalUrl,
            title: metadata.title,
            availableLanguages,
            encryptedSignatureStreamCount: metadata.encryptedSignatureStreams.length,
            decipherStrategy: decipherOutcome.decipherStrategy,
            decipherEnabled: decipherOutcome.decipherEnabled,
            message: decipherOutcome.message,
            fallbackNote: captionFailureMessage,
            actionableFallbacks: decipherOutcome.actionableFallbacks,
          };
        }
      }

      if (!bestAudioStream) {
        return {
          success: false,
          error: source === "audio"
            ? "AUDIO_STREAM_UNAVAILABLE"
            : "NO_CAPTIONS_AND_NO_AUDIO_STREAM",
          videoId,
          canonicalUrl,
          title: metadata.title,
          availableLanguages,
          encryptedSignatureStreamCount: metadata.encryptedSignatureStreams.length,
          message:
            captionFailureMessage
            ? `${captionFailureMessage} Audio fallback was not available for this video.`
            : "No playable audio stream was available for transcription.",
        };
      }

      const audioTranscription = await transcribeAudioUrlWithSharedUtility({
        audioUrl: bestAudioStream.url,
        preferredMimeType: bestAudioStream.mimeType,
        language: args.language,
        includeTimestamps: true,
      });

      if (!audioTranscription.success) {
        return {
          success: false,
          error: audioTranscription.code,
          videoId,
          canonicalUrl,
          title: metadata.title,
          availableLanguages,
          encryptedSignatureStreamCount: metadata.encryptedSignatureStreams.length,
          decipherStrategy: decipherStrategyUsed,
          message:
            audioTranscription.userMessage,
          details: audioTranscription.message,
          fallbackNote: captionFailureMessage,
        };
      }

      const fullTranscript = normalizeCaptionText(audioTranscription.text);
      const truncated = truncateTranscript(fullTranscript, maxCharacters);
      const segments = audioTranscription.segments;
      const transcriptFile = args.saveToUserFiles
        ? await saveTranscriptToUserFiles(ctx, {
            transcript: fullTranscript,
            source: "youtube_audio",
            title: metadata.title,
            language: audioTranscription.detectedLanguage || args.language || null,
            parentPath: args.saveParentPath,
            fileName: args.saveFileName,
            metadata: {
              videoId,
              canonicalUrl,
              sourceUsed: "audio",
            },
          })
        : undefined;

      return {
        success: true,
        sourceUsed: "audio",
        videoId,
        canonicalUrl,
        title: metadata.title,
        language: audioTranscription.detectedLanguage || args.language || null,
        requestedLanguage: args.language || null,
        availableLanguages,
        segmentCount: segments.length,
        transcriptLength: fullTranscript.length,
        maxCharacters,
        isTruncated: truncated.isTruncated,
        transcript: truncated.transcript,
        fallbackNote: captionFailureMessage,
        transcriptFile,
        audioStream: {
          mimeType: bestAudioStream.mimeType,
          bitrate: bestAudioStream.bitrate,
          itag: bestAudioStream.itag,
          fromSignatureCipher: bestAudioStream.fromSignatureCipher,
          audioBytes: audioTranscription.audioBytes,
          audioContentType: audioTranscription.audioContentType,
          decipherStrategy: decipherStrategyUsed,
        },
        segments: args.includeTimestamps
          ? segments.slice(0, maxSegments).map((segment) => ({
              startMs: segment.startMs,
              durationMs: segment.durationMs,
              startTime: formatTimestamp(segment.startMs),
              text: segment.text,
            }))
          : undefined,
      };
    } catch (error) {
      return {
        success: false,
        error: "YOUTUBE_TRANSCRIPT_ERROR",
        videoId,
        message:
          error instanceof Error
            ? error.message
            : "Unknown transcript error.",
      };
    }
  },
};
