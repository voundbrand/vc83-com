const slackTranslations = Object.entries(slackEnValues).map(([suffix, enValue]) => ({
  key: `ui.integrations.slack.${suffix}`,
  values: {
    en: enValue,
    de: slackLocaleOverrides.de[suffix] ?? enValue,
    pl: slackLocaleOverrides.pl[suffix] ?? enValue,
    es: slackLocaleOverrides.es[suffix] ?? enValue,
    fr: slackLocaleOverrides.fr[suffix] ?? enValue,
    ja: slackLocaleOverrides.ja[suffix] ?? enValue,
  },
}));

export const seed = internalMutation({
  handler: async (ctx) => {
    console.log("🌱 Seeding Slack integration translations...");

    const systemOrg = await ctx.db
      .query("organizations")
      .filter((q) => q.eq(q.field("slug"), "system"))
      .first();

    if (!systemOrg) {
      throw new Error("System organization not found. Run seedOntologyData first.");
    }

    const systemUser = await ctx.db
      .query("users")
      .filter((q) => q.eq(q.field("email"), "system@l4yercak3.com"))
      .first();

    if (!systemUser) {
      throw new Error("System user not found. Run seedOntologyData first.");
    }

    const allKeys = slackTranslations.map((translation) => translation.key);

    await getExistingTranslationKeys(ctx.db, systemOrg._id, allKeys);

    let count = 0;
    for (const translation of slackTranslations) {
      for (const locale of supportedLocales) {
        const value = translation.values[locale.code as keyof typeof translation.values];

        if (!value) {
          continue;
        }

        const result = await upsertTranslation(
          ctx.db,
          systemOrg._id,
          systemUser._id,
          translation.key,
          value,
          locale.code,
          "ui",
          "integrations-slack"
        );

        if (result.inserted || result.updated) {
          count += 1;
        }
      }
    }

    console.log(`✅ Seeded ${count} Slack integration translations`);
    return {
      success: true,
      count,
      keys: slackTranslations.length,
    };
  },
});
