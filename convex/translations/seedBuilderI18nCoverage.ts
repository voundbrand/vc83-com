/**
 * Seed Builder Translation Coverage (Lane D)
 *
 * Adds missing ui.builder.* keys introduced in i18n coverage lanes with six-locale parity.
 */
export const seedBuilderI18nCoverage = internalMutation({
  args: {},
  handler: async (ctx) => {
    const systemOrg = await ctx.db
      .query("organizations")
      .filter((q) => q.eq(q.field("slug"), "system"))
      .first();

    if (!systemOrg) {
      throw new Error("System organization not found. Run seedOntologyData first.");
    }

    const systemUser = await ctx.db
      .query("users")
      .filter((q) => q.eq(q.field("email"), "system@l4yercak3.com"))
      .first();

    if (!systemUser) {
      throw new Error("System user not found. Run seedOntologyData first.");
    }

    let inserted = 0;
    let updated = 0;

    for (const translation of translations) {
      for (const locale of SUPPORTED_LOCALES) {
        const result = await upsertTranslation(
          ctx.db,
          systemOrg._id,
          systemUser._id,
          translation.key,
          translation.values[locale],
          locale,
          "builder",
          "lane-d-i18n-coverage",
        );

        if (result.inserted) {
          inserted += 1;
        }

        if (result.updated) {
          updated += 1;
        }
      }
    }

    return {
      success: true,
      keys: translations.length,
      locales: SUPPORTED_LOCALES.length,
      inserted,
      updated,
      totalOperations: translations.length * SUPPORTED_LOCALES.length,
    };
  },
});
